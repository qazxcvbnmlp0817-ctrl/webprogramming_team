    private static final String SEMESTER = "2026-1";

    private final LectureOfferingRepository repository;

    public LectureOffering2026Initializer(LectureOfferingRepository repository) {
        this.repository = repository;
    }

    @Override
    @Transactional
    public void run(String... args) {
        for (String raw : DATA) {
            upsert(raw.split("\\|", -1));
        }
    }

    private void upsert(String[] row) {
        LectureOffering offering = repository.findBySemesterAndCourseCodeAndSection(SEMESTER, row[4], row[6])
                .orElseGet(LectureOffering::new);
        offering.setSemester(SEMESTER);
        offering.setDepartmentName(row[0]);
        offering.setTargetYear(row[1]);
        offering.setCompletionType(row[2]);
        offering.setAreaType(row[3]);
        offering.setCourseCode(row[4]);
        offering.setCourseName(row[5]);
        offering.setSection(row[6]);
        offering.setCredits(num(row[7]));
        offering.setTheoryHours(num(row[8]));
        offering.setDesignHours(num(row[9]));
        offering.setPracticeHours(num(row[10]));
        offering.setTotalHours(num(row[11]));
        offering.setEnrolled(num(row[12]));
        offering.setCapacity(num(row[13]));
        offering.setProfessorName(row[14]);
        offering.setLectureTime(row[15].isBlank() ? "시간미정" : row[15]);
        repository.save(offering);
    }

    private int num(String value) {
        if (value == null || value.isBlank()) return 0;
        return Integer.parseInt(value);
    }

